package album;

public class AlbumDTO {
	private int al_no;
	private String al_code;
	private String al_name;
	private String al_artist;
	private String al_jacket;
	private String al_genre;
	private String al_type;
	private String al_distributor;
	private String al_comp;
	private String al_release;
	private String al_detail;
	
	public AlbumDTO(){}

	public int getAl_no() {
		return al_no;
	}

	public void setAl_no(int al_no) {
		this.al_no = al_no;
	}

	public String getAl_code() {
		return al_code;
	}

	public void setAl_code(String al_code) {
		this.al_code = al_code;
	}

	public String getAl_name() {
		return al_name;
	}

	public void setAl_name(String al_name) {
		this.al_name = al_name;
	}

	public String getAl_artist() {
		return al_artist;
	}

	public void setAl_artist(String al_artist) {
		this.al_artist = al_artist;
	}

	public String getAl_jacket() {
		return al_jacket;
	}

	public void setAl_jacket(String al_jacket) {
		this.al_jacket = al_jacket;
	}

	public String getAl_genre() {
		return al_genre;
	}

	public void setAl_genre(String al_genre) {
		this.al_genre = al_genre;
	}

	public String getAl_type() {
		return al_type;
	}

	public void setAl_type(String al_type) {
		this.al_type = al_type;
	}

	public String getAl_distributor() {
		return al_distributor;
	}

	public void setAl_distributor(String al_distributor) {
		this.al_distributor = al_distributor;
	}

	public String getAl_comp() {
		return al_comp;
	}

	public void setAl_comp(String al_comp) {
		this.al_comp = al_comp;
	}

	public String getAl_release() {
		return al_release;
	}

	public void setAl_release(String al_release) {
		this.al_release = al_release;
	}

	public String getAl_detail() {
		return al_detail;
	}

	public void setAl_detail(String al_detail) {
		this.al_detail = al_detail;
	}
	
	
}//class end
